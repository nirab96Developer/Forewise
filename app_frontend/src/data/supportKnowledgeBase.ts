// פונקציה למציאת מאמר לפי קטגוריה
export const getArticleByCategory = (category: string): SupportArticle | undefined => {
  return supportArticles.find(article => article.category === category);
};

// פונקציה למציאת מאמר לפי ID
export const getArticleById = (id: string): SupportArticle | undefined => {
  return supportArticles.find(article => article.id === id);
};

